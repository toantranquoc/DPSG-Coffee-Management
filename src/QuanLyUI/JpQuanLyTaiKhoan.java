    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        int[] dsXoa = jTableAccount.getSelectedRows();
        if (dsXoa.length <= 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn tài khoản để xóa!");
        }
        else
        {
            ArrayList<Integer> listMataikhoan = new ArrayList<Integer>();
            String maTK = "";
            for (int i : dsXoa) {
                int ma = (int) jTableAccount.getValueAt(i, 0);
                listMataikhoan.add(ma);
                String tenTK = (String) jTableAccount.getValueAt(i, 1);
                maTK += tenTK + " \n";
            }
            int result = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa tài khoản: " + maTK, "Xác nhận xóa", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                boolean isXoa = cn.DeleteTaiKhoan(listMataikhoan);
                if (isXoa) {
                    taikhoan.fillTableTaikhoan();
                    taikhoan.updateUI();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Không xóa được tài khoản!");
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed
    public void fillTableTaikhoan() {
        ArrayList<account> arrayAccount = cn.getListaccount();
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("ID");
        table.addColumn("Tên đăng nhập");
        //table.addColumn("Mật khẩu");
        table.addColumn("Quyền truy cập");
        int tongTk = 0;
        if (arrayAccount.size() > 0) {

            for (account object : arrayAccount) {
                tongTk++;
                table.addRow(new Object[]{object.getId(), object.getUsername(), object.getLoaiTk()});
            }
            jTableAccount.setModel(table);
            lbTongsotaikhoan.setText("Tổng số tài khoản: " + tongTk);
        } else {
            JOptionPane.showMessageDialog(null, "Không có tài khoản!");
            lbTongsotaikhoan.setText("Tổng số tài khoản: " + tongTk);
        }
        for (int i = 0; i < jTableAccount.getColumnCount(); i++) {
            Class<?> col = jTableAccount.getColumnClass(i);
            jTableAccount.setDefaultEditor(col, null);
        }
    }
