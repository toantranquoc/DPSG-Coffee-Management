    private void txtTenTkKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTenTkKeyReleased
        // TODO add your handling code here:
        DefaultTableModel table = new DefaultTableModel();
        table.addColumn("ID");
        table.addColumn("Tên đăng nhập");
        //table.addColumn("Mật khẩu");
        table.addColumn("Quyền truy cập");
        ArrayList<account> arrayAccount = new ArrayList<>();
        arrayAccount = cn.SearchTaiKhoan(txtTenTk.getText());
        int tongtk = 0;
        if (arrayAccount.size() > 0) {

            for (account tk : arrayAccount) {
                tongtk++;
                table.addRow(new Object[]{tk.getId(), tk.getUsername(), tk.getLoaiTk()});
            }
            lbTongsotaikhoan.setText("Tổng số tài khoản: " + tongtk);

        } else {
            lbTongsotaikhoan.setText("Tổng số tài khoản: " + 0);
        }
        jTableAccount.setModel(table);
        for (int i = 0; i < jTableAccount.getColumnCount(); i++) {
            Class<?> col = jTableAccount.getClass();
            jTableAccount.setDefaultEditor(col, null);
        }

    }//GEN-LAST:event_txtTenTkKeyReleased

