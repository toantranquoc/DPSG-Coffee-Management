    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        int row = -1;
        row = JtableMon.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn bàn cần sửa thông tin!");
        } else {
            String maban = (String) JtableMon.getValueAt(row, 0);
            DlSuaMon suaMon = new DlSuaMon(MainUI.ManagerForm.main, true, maban);
            suaMon.setVisible(true);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = JtableMon.getSelectedRows();

        if (selectedRows.length <= 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn món nào !");
        } else {
            ArrayList<String> ListMaBan = new ArrayList<String>();
            String sp = "";
            for (int i : selectedRows) {
                String ma = (String) JtableMon.getValueAt(i, 0);
                ListMaBan.add(ma);
                String tenban = (String) JtableMon.getValueAt(i, 1);

                sp += tenban + "\n";
            }
            int qs;
            qs = JOptionPane.showConfirmDialog(null, "Xóa món: \n " + sp, "Xóa món", JOptionPane.YES_NO_OPTION);
            if (qs == JOptionPane.YES_OPTION) {
                boolean xoa = cn.DeleteThucDon(ListMaBan);
                if (xoa == true) {
                    fillTableMon(null);
                    JOptionPane.showMessageDialog(null, "Xóa món thành công!");
                } else {
                    JOptionPane.showMessageDialog(null, "Không xóa được món !");
                }
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

