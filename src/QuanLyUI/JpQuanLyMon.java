    public void fillTableMon(String ma) {
        DefaultTableModel tbmodel = new DefaultTableModel();
        tbmodel.addColumn("Mã món");
        tbmodel.addColumn("Tên món");
        tbmodel.addColumn("Mã loại");
        tbmodel.addColumn("Đơn giá");
        tbmodel.addColumn("Đơn vị tính");
        ArrayList<ThucDon> arrayMon = new ArrayList<>();
        arrayMon = cn.GetThucDon(ma);
        int tongMon = 0;
        if (arrayMon.size() > 0) {

            for (ThucDon thucdon : arrayMon) {
                tongMon++;
                tbmodel.addRow(new Object[]{thucdon.GetMaMon(), thucdon.GetTenMon(), thucdon.GetMaLoai(), thucdon.GetDonGia(), thucdon.GetDVT()});
            }

        }
        lbTongsomon.setText("Tổng số món: " + tongMon);
        JtableMon.setModel(tbmodel);
        for (int i = 0; i < JtableMon.getColumnCount(); i++) {
            Class<?> col = JtableMon.getColumnClass(i);
            JtableMon.setDefaultEditor(col, null);
        }
    }

    public void fillCbbMon() {
        Vector array = cn.GetNhomMon();
        if (array != null) {
            DefaultComboBoxModel cbbmodel = new DefaultComboBoxModel(array);
            cbbNhommon.setModel(cbbmodel);
        } else {
            JOptionPane.showMessageDialog(null, "Không có nhóm nào !");
        }
    }
